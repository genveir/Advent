        [Test]
        public void SmallExWorks()
        {
            var sol = new Solution(smallEx);
            var computer = sol.computer;

            Assert.AreEqual(1, sol.computer.X);
            computer.Cycle();
            Assert.AreEqual(1, sol.computer.X);
            computer.Cycle();
            Assert.AreEqual(1, sol.computer.X);
            computer.Cycle();
            Assert.AreEqual(4, sol.computer.X);
            computer.Cycle();
            Assert.AreEqual(4, sol.computer.X);
            computer.Cycle();
            Assert.AreEqual(-1, sol.computer.X);
        }

